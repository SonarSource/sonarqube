package org.sonar.server.cvss;

/**
 * Single CVSS metric entry containing the metric name, the selected value
 * (e.g. "Network (N)") and a human readable justification.
 */
public class CvssMetricEntry {

    private CvssMetricName name;
    private String value;
    private String justification;

    public CvssMetricName getName() {
        return name;
    }

    public void setName(CvssMetricName name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getJustification() {
        return justification;
    }

    public void setJustification(String justification) {
        this.justification = justification;
    }
}


